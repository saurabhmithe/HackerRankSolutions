import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Solution {

    public static void main(String[] args) {
        int count = 0;
        Scanner scanner = new Scanner(System.in);
        while(scanner.hasNext()) {
            System.out.println(++count + " " + scanner.nextLine());
        }
    }
}
